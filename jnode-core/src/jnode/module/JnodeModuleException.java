package jnode.module;

/**
 * 
 * @author kreon
 * 
 */
public class JnodeModuleException extends Exception {
	private static final long serialVersionUID = 1L;

	public JnodeModuleException() {
		super();
	}

	public JnodeModuleException(String message, Throwable cause) {
		super(message, cause);
	}

	public JnodeModuleException(String message) {
		super(message);
	}

	public JnodeModuleException(Throwable cause) {
		super(cause);
	}

}
